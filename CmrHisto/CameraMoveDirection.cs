namespace CmrHisto.Enums
{
	/// <summary>
	/// An enumeration that details the direction to move the camera.
	/// </summary>
	public enum CameraMoveDirection
	{
		/// <summary>
		/// Value: 0 - Move the camera to the left.
		/// </summary>
		Left = 0,

		/// <summary>
		/// Value: 0 - Move the camera to the right.
		/// </summary>
		Right = 1,

		/// <summary>
		/// Value: 0 - Move the camera up.
		/// </summary>
		Up = 2,

		/// <summary>
		/// Value: 0 - Move the camera down.
		/// </summary>
		Down = 3,

		/// <summary>
		/// Value: 0 - Move the camera to the left and up.
		/// </summary>
		LeftAndUp = 4,

		/// <summary>
		/// Value: 0 - Move the camera to the left and down.
		/// </summary>
		LeftAndDown = 5,

		/// <summary>
		/// Value: 0 - Move the camera to the right and up.
		/// </summary>
		RightAndUp = 6,

		/// <summary>
		/// Value: 0 - Move the camera to the right and down.
		/// </summary>
		RightAndDown = 7
	}
}
